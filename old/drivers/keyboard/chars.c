static bool keyboard_handleSpecialChar(uint8_t scanCode) {
  if (scanCode == 0x4b) {
    vga_cursorLeftf();
  } else if (scanCode == 0x48) {
    vga_cursorUpf();
  } else if (scanCode == 0x4d) {
    vga_cursorRightf();
  } else if (scanCode == 0x50) {
    vga_cursorDownf();
  } else {
    return false;
  }
  return true;
}
